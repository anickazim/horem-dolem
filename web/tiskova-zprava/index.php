		<p class="lead">Dvojnásobný držitel českého Anděla písničkář Thom Artway, performující výtvarník František Skála s dechovou hudbou Provodovjané, revival Red Hot Chilli Peppers, slam poetry a další desítka umělců z celé ČR vystoupí na východomoravském festivalu Horem Dolem. Dva dny plné kultury s nádechem experimentální tvorby i tradičního folkloru pro dospělé i děti odstartují v sobotu 3. července.</p>
		<p class="lead">Malebná obec Provodov nedaleko Zlína ožije 11. ročníkem multižánrového festivalu Horem Dolem. Ve dnech 3. a 4. července 2021 se zde publiku představí 12 projektů z oblasti hudby, divadla, filmu a mluveného slova.</p>
		<p class="lead">„Už podruhé jsme se rozhodli udělat celý festival na louce pod kopcem Rysov s krásným výhledem na naši dědinu i okolní krajinu Vizovické vrchoviny,“ říká Václav Zimmermann, zakladatel festivalu</p>
		<p class="lead">„Jde nám o návrat k živé kultuře, která pro nás znamená hlavně setkání. Věříme, že se máme s kým setkat – jak s umělci, tak s diváky – a že je potřeba vymyslet, jak má kultura a setkávání po covidovém šoku dál vypadat," dodává Zimmermann. „Festival pořádáme od roku 2007, kdy nám bylo sedumnáct a byli jsme na to jenom dva. Dneska je nám víc a v týmu je nás taky víc. Nejraději jsme, když se tu potkávají tvůrci z našeho kraje s tvůrci odjinud. Jak to třeba vystihuje projekt 'Pojď se mnou, děvče mé' Franty Skály s naší dechovkou, který na Horem Dolem i pokřtíme,“ dodává Matěj Košárek, spoluředitel festivalu.</p>
		<p class="lead">Dvoudenní festival s vůní louky, alternativy a folkloru pořádá 3.–4. července 2021 UnArt Provodov ve spolupráci se spolkem Zlínský Zvěřinec. Vstup na festival je díky úspěšné sbírce na Donio zdarma.</p>

